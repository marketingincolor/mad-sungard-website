<h1 class="orange" style="margin-bottom:0px;">About Window Film</h1>


<h4>What is Window Film?</h4>

Window film is typically tinted and applied professionally to the glass surface of windows in vehicles and buildings 
to promote personal and property protection. Beyond its protective qualities, window films also come in many shades 
and colors to provide aesthetic enhancements, as well.

<h4>Why Use Window Film?</h4>

Using window film in your home, commercial buildings, and vehicles helps to protect you and your property from the 
damaging effects of the sun. Over time, the sun's heat and harmful UV rays contribute to the fading of interior
 furnishings, fabrics, artwork and flooring and can cause uneven or very warm temperatures within a building or 
 vehicle. The sun can also damage your skin, and its glare can cause eyestrain. What's more, window film can hold 
 shards of broken glass together in the event of an accident, vandalism, or break-in.

