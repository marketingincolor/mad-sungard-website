<h4 onclick="toggle_visibility('a2');" class="textbutton" onmouseover="this.style.textDecoration='underline'" onmouseout="this.style.textDecoration='none'">How do Sun-Gard Films work?</h4>
<p id ="a2" class="hidden">By increasing the amount of solar heat that is reflected or absorbed by the film, Sun-Gard products reduce the 
amount of heat that is transmitted through the glass. Reducing solar heat offers a more comfortable interior 
space in a building or vehicle. Utilizing various processes in the construction of our films, allows us to provide 
a varying degree to which our films reduce solar heat transmission. Some films contain a layer of metal that 
provides a high amount of solar heat reflection (even though the film itself is not necessarily reflective). Other 
films contain no metal but still offer a level of heat rejection that will provide significant benefits. Please 
consult your Sun-Gard professional retailer to find out which film is best for you.</p>

<h4 onclick="toggle_visibility('a3');" class="textbutton" onmouseover="this.style.textDecoration='underline'" onmouseout="this.style.textDecoration='none'">How is Sun-Gard Window Film applied?</h4>
<p id ="a3" class="hidden">Sun-Gard window film is professionally installed by a well-trained Sun-Gard dealer. Most of our films are designed 
to be installed on the inside of your windows or glass doors. <br><br>
For architectural applications: The first step is preparation which includes thoroughly cleaning the windows and 
the immediate surrounding work space � this could require moving furniture, removing window treatments and 
protecting the flooring below. Then the film is sprayed with a mounting solution to allow it to be properly 
positioned on the glass. Using professional tools and techniques, the installing dealer will form the film to the 
glass surface being sure to remove any excess water. He�ll then trim the edges of the film for a perfect fit and 
complete the process with a final squeegee technique to ensure the film is in place.<br><br>
For automotive applications: The glass is first thoroughly cleaned. The installing dealer will then wet the film 
and apply it to the outside of the window. Using a heat gun, he will form the film to the exact curvature of the 
window. After this, the film is carefully removed from the outside of the window and applied with a squeegee to the 
inside of the glass. The installing dealer will trim any excess film and will complete the installation by making 
sure the film is firmly in place.</p>

